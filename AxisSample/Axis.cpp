enum class PostionType
{
	LEFTTOP,
	LEFTDOWN,
	RIGHTTOP,
	RIGHTDOWN
};
void SetAxisPosition(PostionType position)
{
		int screenWidth = m_srcWidth;
		int screenHeigh = m_srcHeigh;

		if (position == PostionType::LEFTTOP)
		{
			m_iViewX = 0;
			m_iViewY = screenHeigh - m_iHeight;
		}
		else if (position == PostionType::LEFTDOWN)
		{
			m_iViewX = 0;
			m_iViewY = 0;
		}
		else if (position == PostionType::RIGHTTOP)
		{
			m_iViewX = screenWidth - m_iWidth;
			m_iViewY = screenHeigh - m_iHeight;
		}
		else if (position == PostionType::RIGHTDOWN)
		{
			m_iViewX = screenWidth - m_iWidth;
			m_iViewY = 0;
		}
		else
		{
			m_iViewX = 0;
			m_iViewY = 0;
		}
		
}
