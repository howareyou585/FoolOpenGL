void SetViewSize(int width, int height, float ratio)
{
	//在横竖屏切换时,将坐标轴位置设置标志设置为空，在绘制坐标轴时强制更新显示数据。
	//m_position = AxisNode::NONE;
	//Windows下坐标轴显示太大了，需要控制一下
//#ifdef _WIN32
//	ratio = ratio * 0.5f;
//#else

//#endif
	//坐标轴宽和高
	m_iWidth = width * ratio;
	m_iHeight = height * ratio;

	//让坐标轴靠近边缘显示，距离以宽高的较小值为准
	if (m_iWidth > m_iHeight)
	{
		m_iWidth = m_iHeight;
	}
	else
	{
		m_iHeight = m_iWidth;
	}

	//纪录当前屏幕的宽度和高度
	m_srcWidth = width;
	m_srcHeigh = height;

	//坐标轴显示高度
	m_iW = m_iWidth >= m_iHeight ? m_iHeight : m_iWidth;
}
